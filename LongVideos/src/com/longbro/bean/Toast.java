package com.longbro.bean;

public class Toast {
	private int w_Id;
	private String w_Nick;
	private String w_Tx;
	
	private String w_Content;
	private String w_Ip;
	private String w_Add;
	private String w_Time;
	
	public int getW_Id() {
		return w_Id;
	}
	public void setW_Id(int w_Id) {
		this.w_Id = w_Id;
	}
	public String getW_Nick() {
		return w_Nick;
	}
	public void setW_Nick(String w_Nick) {
		this.w_Nick = w_Nick;
	}
	public String getW_Tx() {
		return w_Tx;
	}
	public void setW_Tx(String w_Tx) {
		this.w_Tx = w_Tx;
	}
	public String getW_Content() {
		return w_Content;
	}
	public void setW_Content(String w_Content) {
		this.w_Content = w_Content;
	}
	public String getW_Ip() {
		return w_Ip;
	}
	public void setW_Ip(String w_Ip) {
		this.w_Ip = w_Ip;
	}
	public String getW_Add() {
		return w_Add;
	}
	public void setW_Add(String w_Add) {
		this.w_Add = w_Add;
	}
	public String getW_Time() {
		return w_Time;
	}
	public void setW_Time(String w_Time) {
		this.w_Time = w_Time;
	}
}
